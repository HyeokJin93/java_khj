package day5;

public class WhileNumberEx1 {

	public static void main(String[] args) {
		/*1부터 5까지 출력하는 코드를 while문으로 작성하세요.
		 * */
		
		for(int i = 1 ; i<=5 ; i++) {
				System.out.print(i + " ");
		}
		
	System.out.println(" ");
	
	int i2 = 1 ;
	while(i2 <= 5) {
		System.out.print(i2 + " ");
		i2++;
	}
	}

}
